
my_tuple = ('C', 'Python', 'C++', 'Java', 'HTML', 'CSS')


slice1 = my_tuple[1:4]
print("Slice from index 1 to 4:", slice1)


slice2 = my_tuple[:3]
print("Slice from start to index 3:", slice2)


slice3 = my_tuple[2:]
print("Slice from index 2 to end:", slice3)


slice4 = my_tuple[::2]
print("Slice with step 2:", slice4)